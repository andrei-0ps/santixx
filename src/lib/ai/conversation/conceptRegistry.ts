export type SemanticConceptId = keyof typeof SEMANTIC_CONCEPTS;

export function conceptTerms(conceptId: SemanticConceptId) {
  return [...SEMANTIC_CONCEPTS[conceptId].terms];
}

export function conceptLabels(conceptIds: readonly SemanticConceptId[], locale: "ro" | "en") {
  return conceptIds.map((conceptId) => SEMANTIC_CONCEPTS[conceptId].label[locale]);
}

export function conceptPlaceholder(conceptIds: readonly SemanticConceptId[]) {
  const conceptId = conceptIds[0];
  if (!conceptId) return undefined;
  return {
    ro: `Ex.: ${SEMANTIC_CONCEPTS[conceptId].example.ro}`,
    en: `E.g. ${SEMANTIC_CONCEPTS[conceptId].example.en}`,
  };
}
