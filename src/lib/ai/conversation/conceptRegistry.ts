export type SemanticConceptDefinition = {
  label: { ro: string; en: string };
  example: { ro: string; en: string };
  terms: readonly string[];
};

function concept(
  labelRo: string,
  labelEn: string,
  exampleRo: string,
  exampleEn: string,
  terms: readonly string[],
): SemanticConceptDefinition {
  return {
    label: { ro: labelRo, en: labelEn },
    example: { ro: exampleRo, en: exampleEn },
    terms,
  };
}

